#!/usr/bin/env bash
# Initialize a new React + TS + Vite + Tailwind + shadcn/ui artifact project.
# Usage: bash scripts/init-artifact.sh <project-name>
set -euo pipefail

if [ $# -lt 1 ]; then
  echo "Usage: $0 <project-name>" >&2
  exit 1
fi

PROJECT_NAME="$1"

if [ -e "$PROJECT_NAME" ]; then
  echo "Error: '$PROJECT_NAME' already exists." >&2
  exit 1
fi

# Detect Node major version; pin Vite to 5.x for Node 18, otherwise latest 5.x.
NODE_MAJOR="$(node -p 'process.versions.node.split(".")[0]' 2>/dev/null || echo 0)"
if [ "$NODE_MAJOR" -lt 18 ]; then
  echo "Error: Node 18+ required (found $NODE_MAJOR)." >&2
  exit 1
fi
VITE_VERSION="^5.4.0"

mkdir -p "$PROJECT_NAME"
cd "$PROJECT_NAME"

